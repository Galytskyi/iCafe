#include "wassert.h"

#include <assert.h>

// -------------------------------------------------------------------------------------------------------------------

void wassert(int arg)
{
	if (ENABLE_ASSERT == true)
	{
		assert(arg);
	}
}

// -------------------------------------------------------------------------------------------------------------------

void wassert(bool arg)
{
	if (ENABLE_ASSERT == true)
	{
		assert(arg);
	}
}

